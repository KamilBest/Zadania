/**
 * Player class, contains the player's name.
 *
 * @author Kamil Best
 */
class Player {
    private String playerName;

    Player(String name) {
        playerName = name;
    }

    /**
     * The method returns player name.
     *
     * @return String playerName
     */
    public String getPlayerName() {
        return playerName;
    }
}
